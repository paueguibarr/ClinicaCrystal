    private void btnActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnActualizarActionPerformed
        // TODO add your handling code here:
        try {
            //read input to updtate
            String paciente = txtNombre.getSelectedItem().toString();
            String cant = txtCant.getText();
            String mat = txtMat.getText();
            String precioU = txtprecioU.getText();
            String sub = txtSub.getText();
            String iva = txtIva.getText();  
            String id = lblId.getText();
           
            //make the updates 
            pst = conn.getConnection().prepareStatement("update cuentas set material = ?, cantidad = ?, precio = ?, subtotal = ?, precioIva = ? where id = ?");
            pst.setString(1,mat);
            pst.setDouble(2, Double.parseDouble(cant));
            pst.setDouble(3, convertToNo(precioU));
            pst.setDouble(4, convertToNo(sub));
            pst.setDouble(5, convertToNo(iva));      
            pst.setInt(6,Integer.parseInt(id));
            pst.executeUpdate();
            JOptionPane.showMessageDialog(this,"Cuenta Actualizada");
        } catch(SQLException e) {
            Logger.getLogger(EditarCuenta.class.getName()).log(Level.SEVERE, null, e);
            JOptionPane.showMessageDialog(this,"Cuenta NO Actualizada, intente de nuevo");
        }
        updateSubtotal();
        updateIva();
        updateCuentaReal();       
        createTable();
    }//GEN-LAST:event_btnActualizarActionPerformed

