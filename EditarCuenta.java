    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        // TODO add your handling code here:
        // get id 
        int id = Integer.parseInt(lblId.getText());
        try {
            //delete item that matches that id
            pst = conn.getConnection().prepareStatement("delete from `cuentas` WHERE `id`= ?");
            pst.setInt(1, id);
            pst.executeUpdate();
            JOptionPane.showMessageDialog(this,"Material Eliminado Exitosamente");
            
            //generate updated table
            createTable();
            
            //update cuentas
            updateSubtotal();
            updateIva();
            updateCuentaReal();
            
        } catch (SQLException ex) {
            Logger.getLogger(EditarCuenta.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnEliminarActionPerformed
    
    //load pacientes actuales
    public void loadPaciente() {
        try {
            pst = conn.getConnection().prepareStatement("SELECT `nombre` FROM `Paciente` WHERE `egreso` IS NULL");
            rs = pst.executeQuery();
            txtNombre.removeAll(); 
            
            while(rs.next()) {
                txtNombre.addItem(rs.getString(1));
            }
            
        } catch (SQLException ex) {
            Logger.getLogger(EditarCuenta.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
    //remove "$" from number to add it to DB
    public double convertToNo(String valueString) {
        String trimmedString = valueString.replace("$", "");
        trimmedString = trimmedString.trim();
        double value = Double.parseDouble(trimmedString); 
        return value;
    }
    
    public void updateCuentaReal() {
        String paciente = txtNombre.getSelectedItem().toString();
        // this try and catch will update the cuenta real of the pacient after adding something to the individual bill
        // by adding all precio + iva
        try {
            pst = conn.getConnection().prepareStatement("UPDATE `Paciente` SET `Paciente`.`cuenta real` = (SELECT SUM(`cuentas`.`precioIva`) FROM `cuentas` WHERE `cuentas`.`paciente` = ?) WHERE `Paciente`.`nombre` = ?");
            pst.setString(1, paciente);
            pst.setString(2,paciente);
            pst.executeUpdate();
     
        } catch (SQLException ex) {
            Logger.getLogger(EditarCuenta.class.getName()).log(Level.SEVERE, null, ex);
            
        }
    }
    
    //display the bill
    public void createTable() {
        String paciente = txtNombre.getSelectedItem().toString();
        
         try {
            pst = conn.getConnection().prepareStatement("SELECT `id`,`cantidad`,`material`,`precio`, `subtotal`,`precioIva` FROM `cuentas` WHERE `paciente` = ?");
            pst.setString(1, paciente);
            rs = pst.executeQuery();
            ResultSetMetaData Rsm = rs.getMetaData();
            int c;
            c = Rsm.getColumnCount();            
            DefaultTableModel df = (DefaultTableModel)tableEC.getModel();
            df.setRowCount(0);                    
            while (rs.next()) {
                Vector v2 = new Vector();
                for(int i = 1; i <= c; i++) {
                    v2.add(rs.getString("id"));
                    v2.add(rs.getString("cantidad"));
                    v2.add(rs.getString("material"));
                    v2.add("$" + rs.getString("precio"));
                    v2.add("$" + rs.getString("subtotal"));
                    v2.add("$" + rs.getString("precioIva"));
          
                }
                        
                df.addRow(v2);
                                                         
            }
            

         } catch (SQLException ex) {
            Logger.getLogger(Historial.class.getName()).log(Level.SEVERE, null, ex);
         }
    }
    
    // update subtotal to multiply the new number of materials
    public void updateSubtotal() {
        try {
                pst = conn.getConnection().prepareStatement("UPDATE `cuentas` SET `cuentas`.`subtotal` = `cuentas`.`precio` * `cuentas`.`cantidad`");
                pst.executeUpdate();
            
            } catch (SQLException ex) {
                Logger.getLogger(CuentaHosp.class.getName()).log(Level.SEVERE, null, ex);
            }
    }
    //update iva by multiplying subtotal * 1.16 if its not med or sol
    public void updateIva() {
        try {
                
                pst = conn.getConnection().prepareStatement("UPDATE `cuentas` SET `cuentas`.`precioIva` = `cuentas`.`subtotal` * 1.16 where `cuentas`.`iva` = 'Y' ");
                pst.executeUpdate();
                pst = conn.getConnection().prepareStatement("UPDATE `cuentas` SET `cuentas`.`precioIva` = `cuentas`.`subtotal` where `cuentas`.`iva` = 'N' ");
                pst.executeUpdate();
                
            
            } catch (SQLException ex) {
                Logger.getLogger(CuentaHosp.class.getName()).log(Level.SEVERE, null, ex);
            }
    }
    
