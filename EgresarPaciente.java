    private void btnOKActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnOKActionPerformed
        try {
            // TODO add your handling code here:
            String nombre = boxNombre.getSelectedItem().toString();
            pst = conn.getConnection().prepareStatement("SELECT `estatus` FROM `Paciente` WHERE `nombre` = ?");
            pst.setString(1, nombre);
            rs = pst.executeQuery(); 
            boxNombre.removeAll();
            
            while(rs.next()) {
                lblEstatus.setText(rs.getString(1));
            }
        } catch (SQLException ex) {
            Logger.getLogger(EgresarPaciente.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnOKActionPerformed
    //set todays date automatically
    public void setDate() {
        DateTimeFormatter formatter = DateTimeFormatter.ofPattern("yyyy-MM-dd");

        // Get today's date
        LocalDate currentDate = LocalDate.now();

        // Format the date as a string
        String formattedDate = currentDate.format(formatter);

        
        txtFechaEgreso.setText(formattedDate);
    }
    
    //loads active patients 
    public void loadPacienteActuales() {
        try {
            pst = conn.getConnection().prepareStatement("SELECT `nombre` FROM `Paciente`");
            rs = pst.executeQuery();
            boxNombre.removeAll(); 
            
            while(rs.next()) {
                boxNombre.addItem(rs.getString(1));
            }
                       
            
        } catch (SQLException ex) {
            Logger.getLogger(EgresarPaciente.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
