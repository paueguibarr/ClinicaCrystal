    private void btnAceptarEPActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAceptarEPActionPerformed
        // TODO add your handling code here:
        
        //read inputs
        String paciente = boxNombre.getSelectedItem().toString();
        String diag = txtDiag.getText();
        String egreso = txtFechaEgreso.getText();
        String fact = txtFact.getText();
        String recibo = txtRecibo.getText();
        try {
            // add fecha egreso, diagnostico egreso and estatus of egresado
            pst = conn.getConnection().prepareStatement("UPDATE `Paciente` SET `egreso` = ?, `diagnosticoEgreso` = ?, `estatus` = ? WHERE `nombre` = ?");
            pst.setString(1, egreso);
            pst.setString(2, diag);
            pst.setString(3, "egresado");
            pst.setString(4, paciente);
            pst.executeUpdate(); 

            //if fact != null, add it to the paciente 
            if(!fact.equals("-")) {
                pst = conn.getConnection().prepareStatement("select facturas from Paciente where nombre = ?");
                pst.setString(1, paciente);
                rs = pst.executeQuery();
                if(rs.next()) {
                    String res = rs.getString("facturas");
                    if(res == null) {
                        pst = conn.getConnection().prepareStatement("update Paciente set facturas = ? where nombre = ?");
                        pst.setString(1, fact);
                        pst.setString(2, paciente);
                        pst.executeUpdate();
                    } else {
                        pst = conn.getConnection().prepareStatement("update Paciente set facturas = CONCAT(facturas, ' ',?) where nombre = ?");
                        pst.setString(1, fact);
                        pst.setString(2, paciente);
                        pst.executeUpdate();
                    }
                }       
            }  
            
            //if recibo != null, add it to the paciente 
            if(!recibo.equals("-")) {
                pst = conn.getConnection().prepareStatement("select recibo from Paciente where nombre = ?");
                pst.setString(1, paciente);
                rs = pst.executeQuery();
                if(rs.next()) {
                    String res = rs.getString("recibo");
                    if(res == null) {
                        pst = conn.getConnection().prepareStatement("update Paciente set recibo = ? where nombre = ?");
                        pst.setString(1, recibo);
                        pst.setString(2, paciente);
                        pst.executeUpdate();
                    } else {
                        pst = conn.getConnection().prepareStatement("update Paciente set recibo = CONCAT(recibo, ' ',?) where nombre = ?");
                        pst.setString(1, recibo);
                        pst.setString(2, paciente);
                        pst.executeUpdate();
                    }
                }       
            } 
                       
            JOptionPane.showMessageDialog(this, "Paciente egresado exitosamente");
            
        } catch (SQLException ex) {
            Logger.getLogger(EgresarPaciente.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(this, "Paciente NO egresado, intente de nuevo");

        }
        
        
        //recet parameters
        boxNombre.setSelectedIndex(-1);
        txtDiag.setText("");
        txtRecibo.setText("");
        txtFact.setText("");
        boxNombre.removeAllItems();
        loadPacienteActuales();
        
    }//GEN-LAST:event_btnAceptarEPActionPerformed

