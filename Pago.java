    private void btnOKPaqActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnOKPaqActionPerformed
        // TODO add your handling code here:
        //read inputs of paquete
        Double paq = Double.valueOf(txtPaq.getText());
        String paciente = txtNombre.getSelectedItem().toString();
        try {
            //update paquete
            pst = conn.getConnection().prepareStatement("update Paciente set paquete = ? where nombre = ?");
            pst.setDouble(1, paq);
            pst.setString(2, paciente);
            pst.executeUpdate();
            
            //update diferencia
            pst = conn.getConnection().prepareStatement("update Paciente set diferencia = `cuenta real` - `paquete` where nombre = ?");
            pst.setString(1, paciente);
            pst.executeUpdate();
            
            // display diferencia
            pst = conn.getConnection().prepareStatement("SELECT `diferencia` FROM `Paciente` WHERE `nombre` = ?");
            pst.setString(1,paciente);
            rs = pst.executeQuery();   
            if (rs.next()) {
                txtDif.setText("$"+rs.getString("diferencia"));
            }
            JOptionPane.showMessageDialog(this,"Paquete Agregado");
            
        } catch (SQLException ex) {
            Logger.getLogger(Pago.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(this,"Paquete NO Agregado, intente de nuevo");
        }
        
        
        
        
    }//GEN-LAST:event_btnOKPaqActionPerformed

