    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed
        try {
            // TODO add your handling code here:
            //read inputs
            String fecha = txtFecha.getText();
            String paciente = txtNombre.getSelectedItem().toString();
            String tipo = boxPago.getSelectedItem().toString();
            double monto = Double.parseDouble(txtCant.getText());
            
            //instert into pagos DB
            pst = conn.getConnection().prepareStatement("insert into pagos(paciente,monto,tipo,fecha)values(?,?,?,?)");
            pst.setString(1, paciente);
            pst.setDouble(2, monto);
            pst.setString(3, tipo);
            pst.setString(4, fecha);
            pst.executeUpdate();
            
            
            // output success
            JOptionPane.showMessageDialog(this,"Pago Agregado");
            
            //update cuenta real
            pst = conn.getConnection().prepareStatement("UPDATE `Paciente` SET `Paciente`.`cuenta real` = `Paciente`.`cuenta real` - ?  WHERE `Paciente`.`nombre` = ?");
            pst.setDouble(1, monto);
            pst.setString(2,paciente);
            pst.executeUpdate();
     
                  
        } catch (SQLException ex) {
            Logger.getLogger(Pago.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(this,"Pago NO Agregado, intente de nuevo");
        }
        
        //method
        displayPago();
        
    }//GEN-LAST:event_btnAgregarActionPerformed

